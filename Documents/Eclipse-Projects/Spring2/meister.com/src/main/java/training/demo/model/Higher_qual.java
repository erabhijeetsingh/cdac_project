package training.demo.model;

public class Higher_qual {
	int hq_id;
	String h_name;
	public int getHq_id() {
		return hq_id;
	}
	public void setHq_id(int hq_id) {
		this.hq_id = hq_id;
	}
	public String getH_name() {
		return h_name;
	}
	public void setH_name(String h_name) {
		this.h_name = h_name;
	}
	@Override
	public String toString() {
		return "Higher_qual [hq_id=" + hq_id + ", h_name=" + h_name + "]";
	}
	
	
}
